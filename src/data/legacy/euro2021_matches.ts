// Synthetic generator that perfectly respects the outcome parameters for data populating purposes
const generateSyntheticBets = (matches: any[], stats: any) => {
    const bets = [];
    let buchasLeft = stats.exact_scores;
    let outcomesLeft = stats.outcomes;
    
    // Distribute matches randomly
    const matchIndices = Array.from({ length: matches.length }, (_, i) => i);
    matchIndices.sort(() => Math.random() - 0.5);

    for (let i = 0; i < matches.length; i++) {
        const m = matches[matchIndices[i]];
        const isHomeWin = m.homeScore > m.awayScore;
        const isTie = m.homeScore === m.awayScore;
        const isAwayWin = m.homeScore < m.awayScore;

        if (buchasLeft > 0) {
            bets.push({ matchIndex: matchIndices[i], home: m.homeScore, away: m.awayScore });
            buchasLeft--;
        } else if (outcomesLeft > 0) {
            let guessH = m.homeScore;
            let guessA = m.awayScore;
            
            // Adjust to be a correct situation but NOT an exact score
            if (isHomeWin) {
                guessH += 1;
            } else if (isTie) {
                guessH += 1; 
                guessA += 1;
            } else {
                guessA += 1;
            }
            bets.push({ matchIndex: matchIndices[i], home: guessH, away: guessA });
            outcomesLeft--;
        } else {
            // Must be an error. Make sure the outcome is different!
            let errH = 0;
            let errA = 0;
            if (isHomeWin) { errA = 1; }
            else if (isTie) { errH = 1; }
            else { errH = 1; }
            bets.push({ matchIndex: matchIndices[i], home: errH, away: errA });
        }
    }

    // Sort bets back by index for matching visually
    bets.sort((a, b) => a.matchIndex - b.matchIndex);
    return bets;
};

