function calc(name, preds, reals) {
    let pts = 0;
    let exacts = 0;
    let outcomes = 0;
    preds.forEach((p, i) => {
        if (i >= reals.length) return;
        const r = reals[i];
        const pWin = p[0] > p[1] ? 1 : (p[0] < p[1] ? 2 : 0);
        const rWin = r[0] > r[1] ? 1 : (r[0] < r[1] ? 2 : 0);
        if (p[0] === r[0] && p[1] === r[1]) {
            pts += 3; exacts++;
        } else if (pWin === rWin) {
            pts += 1; outcomes++;
        }
    });
    console.log(`${name}: { pts: ${pts}, exacts: ${exacts}, outcomes: ${outcomes}, count: ${preds.length} }`);
}

