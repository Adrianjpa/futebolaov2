function calc(name, preds, reals) {
    let pts = 0;
    preds.forEach((p, i) => {
        if (i >= reals.length) return;
        const r = reals[i];
        const pWin = p[0] > p[1] ? 1 : (p[0] < p[1] ? 2 : 0);
        const rWin = r[0] > r[1] ? 1 : (r[0] < r[1] ? 2 : 0);
        if (p[0] === r[0] && p[1] === r[1]) {
            pts += 3;
        } else if (pWin === rWin) {
            pts += 1;
        }
    });
    console.log(`${name}: ${pts}`);
}

