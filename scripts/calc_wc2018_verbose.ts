function calcVerbose(name, preds, reals) {
    let pts = 0;
    preds.forEach((p, i) => {
        if (i >= reals.length) return;
        const r = reals[i];
        const pWin = p[0] > p[1] ? 1 : (p[0] < p[1] ? 2 : 0);
        const rWin = r[0] > r[1] ? 1 : (r[0] < r[1] ? 2 : 0);
        let matchPts = 0;
        if (p[0] === r[0] && p[1] === r[1]) {
            matchPts = 3;
        } else if (pWin === rWin) {
            matchPts = 1;
        }
        pts += matchPts;
        console.log(`Match ${i + 1}: Real [${r}] Pred [${p}] -> ${matchPts} pts`);
    });
    console.log(`${name} Total: ${pts}`);
}

